# Per-response user turn — same for both dimensions.
# {sjt_response} is replaced with each participant's text at inference time.
USER_PROMPT_TEMPLATE = "Workplace scenario response:\n\"{sjt_response}\"\n\nRating:"

# RATING FUNCTION

def build_prompt(sjt_response: str, prompt: str) -> str:
    messages = [
        {"role": "system", "content": SYSTEM_PROMPTS[prompt]},
        {"role": "user",   "content": USER_PROMPT_TEMPLATE.format(sjt_response=sjt_response)},
    ]
    return tokenizer.apply_chat_template(
        messages,
        tokenize=False,
        add_generation_prompt=True,
    )


def get_bimi_rating(sjt_response: str, prompt: str) -> int | None:
    formatted_prompt = build_prompt(sjt_response, prompt)
    inputs = tokenizer(formatted_prompt, return_tensors="pt").to(device)

    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens=5,
            do_sample=False,
            pad_token_id=tokenizer.eos_token_id,
        )

    new_tokens = outputs[0][inputs["input_ids"].shape[1]:]
    raw_output = tokenizer.decode(new_tokens, skip_special_tokens=True).strip()

    match = re.search(r'\b([1-7])\b', raw_output)
    if match:
        return int(match.group(1))

    print(f"  Warning: unexpected output '{raw_output}' for: {sjt_response[:60]}...")
    return None

# RATING LOOP

all_results = []

for run_idx, (classification, dimension, text_col, prompt) in enumerate(RUNS, 1):

    print(f"\n{'='*60}")
    print(f"Run {run_idx}/{len(RUNS)}: {classification} / {dimension} / {text_col} / prompt={prompt}")
    print(f"{'='*60}")

    # Load the correct data file for this classification × dimension
    data_path = f"{DATA_DIR}/{classification}_{dimension}.csv"
    df = pd.read_csv(data_path)

    if text_col not in df.columns:
        print(f"  WARNING: column '{text_col}' not found in {data_path}. Skipping.")
        continue

    df_clean = df[[text_col, "BIMI_mean", "ResponseId"]].dropna().reset_index(drop=True)
    print(f"  Responses to rate: {len(df_clean)}")

    ratings      = []
    failed_count = 0

    for i, row in df_clean.iterrows():
        rating = get_bimi_rating(row[text_col], prompt)

        if rating is None:
            failed_count += 1
            ratings.append(np.nan)
        else:
            ratings.append(rating)

        if (i + 1) % 50 == 0:
            print(f"  Rated {i + 1}/{len(df_clean)}...")

    df_clean["predicted_BIMI"] = ratings
    df_clean["actual_BIMI"]    = df_clean["BIMI_mean"]
    df_clean["classification"] = classification
    df_clean["dimension"]      = dimension
    df_clean["text_input"]     = text_col
    df_clean["prompt"]         = prompt
    df_clean["model"]          = MODEL_ID
    df_clean["model_revision"] = model_revision
    df_clean["script_version"] = SCRIPT_VERSION

    df_results = df_clean.dropna(subset=["predicted_BIMI"]).reset_index(drop=True)

    pearson_r, p_value = pearsonr(df_results["actual_BIMI"], df_results["predicted_BIMI"])
    mse                = mean_squared_error(df_results["actual_BIMI"], df_results["predicted_BIMI"])
    rmse               = np.sqrt(mse)

    df_results["pearson_r"] = pearson_r
    df_results["p_value"]   = p_value

    print(f"  Successful: {len(df_results)} / {len(df_clean)} | Failed: {failed_count}")
    print(f"  Pearson r = {pearson_r:.3f}, p = {p_value:.4f}, RMSE = {rmse:.4f}")
    print(f"  Distribution:\n{df_results['predicted_BIMI'].value_counts().sort_index().to_string()}")

    # Save individual run CSV
    safe_model_name = MODEL_ID.replace("/", "_")
