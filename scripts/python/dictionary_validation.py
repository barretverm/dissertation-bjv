# DEFINE FUNCTIONS

def strip_wildcards(word: str) -> str:
    """Remove LIWC-style wildcards (* and .) from word stems."""
    return re.sub(r"[\*\.\+]$", "", str(word).strip())

def load_dictionary(filepath: str) -> list:
    """
    Load a single-column CSV dictionary.
    Recovers the header row as a word (LIWC CSVs use first word as header).
    Strips wildcards, lowercases, deduplicates. Returns a sorted list.
    """
    df = pd.read_csv(filepath, header=0, dtype=str)
    header_word = df.columns[0]
    words = [header_word] + df.iloc[:, 0].dropna().tolist()
    cleaned = list({strip_wildcards(w).lower().strip()
                    for w in words if w.strip() != ""})
    return sorted(cleaned)

def cosine_similarity(a: np.ndarray, b: np.ndarray) -> float:
    """Cosine similarity between two average vectors."""
    denom = np.linalg.norm(a) * np.linalg.norm(b)
    if denom == 0:
        return 0.0
    return float(np.dot(a, b) / denom)

def pairwise_cosine_matrix(vectors: np.ndarray) -> np.ndarray:
    """Full pairwise cosine similarity matrix for a set of vectors."""
    norms = np.linalg.norm(vectors, axis=1, keepdims=True)
    norms = np.where(norms == 0, 1e-10, norms)
    normed = vectors / norms
    return normed @ normed.T

def cronbach_alpha_cosine(sim_matrix: np.ndarray) -> float:
    """
    Cronbach's alpha analog using cosine similarities as inter-item correlations.
    Follows Nicolas et al. (2020) / Chakrabartty (2018).
    Note: inflated for very large n; interpret with caution.
    """
    n = sim_matrix.shape[0]
    if n < 2:
        return np.nan
    upper = sim_matrix[np.triu_indices(n, k=1)]
    mean_r = np.mean(upper)
    alpha = (n * mean_r) / (1 + (n - 1) * mean_r)
    return float(alpha)

def reliability_stats(name: str, words: list, word2vec: dict) -> dict:
    """Compute reliability statistics for a single dictionary."""
    vecs = np.array([word2vec[w] for w in words if w in word2vec])
    n = len(vecs)
    if n < 2:
        return None
    sim_matrix = pairwise_cosine_matrix(vecs)
    upper = sim_matrix[np.triu_indices(n, k=1)]
    within_mean = float(np.mean(upper))
    within_sd   = float(np.std(upper))
    alpha       = cronbach_alpha_cosine(sim_matrix)
    return {
        "Dictionary"      : name,
        "N_words"         : n,
        "Mean_within_sim" : round(within_mean, 4),
        "SD_within_sim"   : round(within_sd, 4),
        "Cronbach_alpha"  : round(alpha, 4),
    }

def ttest_within_between(target_names: list,
                         all_dict_names: list,
                         dictionaries: dict,
                         dict_vectors: dict,
                         word2vec: dict,
                         label: str) -> dict:
    """
    t-test: within-dictionary similarity > between-dictionary similarity.
    Replicates Nicolas et al. (2020) approach.
    """
    all_within = []
    for name in target_names:
        words = dictionaries[name]
        vecs  = np.array([word2vec[w] for w in words if w in word2vec])
        if len(vecs) < 2:
            continue
        sim_matrix = pairwise_cosine_matrix(vecs)
        n = len(vecs)
        upper = sim_matrix[np.triu_indices(n, k=1)]
        all_within.extend(upper.tolist())

    all_between = []
    names = list(all_dict_names)
    for i, n1 in enumerate(names):
        for j, n2 in enumerate(names):
            if j <= i:
                continue
            if n1 in dict_vectors and n2 in dict_vectors:
                sim = cosine_similarity(dict_vectors[n1], dict_vectors[n2])
                all_between.append(sim)

    t_stat, p_val = stats.ttest_ind(all_within, all_between,
                                    alternative="greater")
    pooled_sd = np.std(all_within + all_between)
    d = (np.mean(all_within) - np.mean(all_between)) / pooled_sd if pooled_sd > 0 else np.nan

    print(f"\n  t-test within > between [{label}]:")
    print(f"    M_within  = {np.mean(all_within):.4f}  (n = {len(all_within):,})")
    print(f"    M_between = {np.mean(all_between):.4f}  (n = {len(all_between):,})")
    print(f"    t = {t_stat:.4f}, p = {p_val:.4f}, d = {d:.4f}")

    return {
        "label"     : label,
        "M_within"  : round(np.mean(all_within), 4),
        "M_between" : round(np.mean(all_between), 4),
        "t"         : round(t_stat, 4),
        "p"         : round(p_val, 4),
        "d"         : round(d, 4),
    }

