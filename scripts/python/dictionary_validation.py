def plot_heatmap(sim_df, title, save_path, n_target, n_convergent, n_discriminant):
    """Plot and save a labeled cosine similarity heatmap."""
    dict_names    = list(sim_df.index)
    n_dicts       = len(dict_names)
    display_labels = [n.replace("_", " ") for n in dict_names]

    fig, ax = plt.subplots(figsize=(13, 10))

    sns.heatmap(
        sim_df,
        annot=True,
        fmt=".3f",
        cmap="RdYlGn",
        vmin=0,
        vmax=1,
        linewidths=0.5,
        linecolor="white",
        square=True,
        xticklabels=display_labels,
        yticklabels=display_labels,
        ax=ax,
        cbar_kws={"label": "Cosine Similarity", "shrink": 0.8}
    )

    ax.set_title(title, fontsize=14, fontweight="bold", pad=15)
    ax.set_xticklabels(ax.get_xticklabels(), rotation=45, ha="right", fontsize=10)
    ax.set_yticklabels(ax.get_yticklabels(), rotation=0, fontsize=10)

    # Dividing lines between groups
    boundary_1 = n_target
    boundary_2 = n_target + n_convergent
    for line_pos in [boundary_1, boundary_2]:
        ax.axhline(y=line_pos, color="black", linewidth=2)
        ax.axvline(x=line_pos, color="black", linewidth=2)

    # Group labels on right side
    group_labels = [
        (n_target / 2,                              "Target"),
        (n_target + n_convergent / 2,               "Convergent"),
        (n_target + n_convergent + n_discriminant / 2, "Discriminant"),
    ]
    for y_pos, label in group_labels:
        ax.text(
            n_dicts + 0.3, y_pos, label,
            va="center", ha="left", fontsize=9,
            fontstyle="italic", color="dimgray"
        )

    plt.tight_layout()
    plt.savefig(save_path, dpi=300, bbox_inches="tight")
    plt.show()
    print(f"  Saved: {save_path}")


