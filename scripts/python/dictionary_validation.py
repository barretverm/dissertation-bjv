def compute_dict_vectors(dictionaries: dict, word2vec: dict) -> dict:
    vectors = {}
    for name, words in dictionaries.items():
        vecs = np.array([word2vec[w] for w in words if w in word2vec])
        if len(vecs) == 0:
            print(f"  WARNING: No embeddings found for {name}")
            continue
        vectors[name] = vecs.mean(axis=0)
    return vectors

