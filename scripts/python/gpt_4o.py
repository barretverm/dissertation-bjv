USER_PROMPT_TEMPLATE = "Workplace scenario response:\n\"{sjt_response}\"\n\nRating:"

# RATING FUNCTION

# Minimum delay between requests to respect rate limit
REQUEST_DELAY = 60.0 / REQUESTS_PER_MINUTE

def get_bimi_rating(sjt_response: str, prompt: str,
                    max_retries: int = 5) -> int | None:
    """
    Sends a single SJT response to the GPT-4o API and returns an integer
    rating from 1–7. Implements exponential backoff for rate limit errors.
    Returns None if no valid integer can be parsed after all retries.
    """
    user_content = USER_PROMPT_TEMPLATE.format(sjt_response=sjt_response)

    for attempt in range(max_retries):
        try:
            response = client.chat.completions.create(
                model=MODEL_ID,
                messages=[
                    {"role": "system", "content": SYSTEM_PROMPTS[prompt]},
                    {"role": "user",   "content": user_content},
                ],
                max_tokens=5,
                temperature=0.0,    # greedy decoding — deterministic output
            )
            raw_output = response.choices[0].message.content.strip()

            # Extract first valid 1–7 integer
            match = re.search(r'\b([1-7])\b', raw_output)
            if match:
                time.sleep(REQUEST_DELAY)
                return int(match.group(1))

            print(f"  Warning: unexpected output '{raw_output}' "
                  f"for: {sjt_response[:60]}...")
            time.sleep(REQUEST_DELAY)
            return None

        except Exception as e:
            error_str = str(e).lower()
            if "rate_limit" in error_str or "429" in error_str:
                wait = (2 ** attempt) * 5   # 5s, 10s, 20s, 40s, 80s
                print(f"  Rate limit hit — waiting {wait}s before retry "
                      f"(attempt {attempt + 1}/{max_retries})...")
                time.sleep(wait)
            else:
                print(f"  API error: {e}")
                time.sleep(REQUEST_DELAY)
                return None

    print(f"  Max retries exceeded for: {sjt_response[:60]}...")
    return None

