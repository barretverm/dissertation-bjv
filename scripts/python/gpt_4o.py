def get_bimi_rating_tracked(sjt_response: str, prompt: str) -> int | None:
    """
    Wrapper around get_bimi_rating that also tracks token usage for cost
    estimation. Updates global token counters.
    """
    global total_input_tokens, total_output_tokens

    user_content = USER_PROMPT_TEMPLATE.format(sjt_response=sjt_response)

    for attempt in range(5):
        try:
            response = client.chat.completions.create(
                model=MODEL_ID,
                messages=[
                    {"role": "system", "content": SYSTEM_PROMPTS[prompt]},
                    {"role": "user",   "content": user_content},
                ],
                max_tokens=5,
                temperature=0.0,
            )
            raw_output = response.choices[0].message.content.strip()

            # Track token usage
            if response.usage:
                total_input_tokens  += response.usage.prompt_tokens
                total_output_tokens += response.usage.completion_tokens

            match = re.search(r'\b([1-7])\b', raw_output)
            if match:
                time.sleep(REQUEST_DELAY)
                return int(match.group(1))

            print(f"  Warning: unexpected output '{raw_output}' "
                  f"for: {sjt_response[:60]}...")
            time.sleep(REQUEST_DELAY)
            return None

        except Exception as e:
            error_str = str(e).lower()
            if "rate_limit" in error_str or "429" in error_str:
                wait = (2 ** attempt) * 5
                print(f"  Rate limit hit — waiting {wait}s "
                      f"(attempt {attempt + 1}/5)...")
                time.sleep(wait)
            else:
                print(f"  API error: {e}")
                time.sleep(REQUEST_DELAY)
                return None

    return None

